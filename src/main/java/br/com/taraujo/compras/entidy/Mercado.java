package br.com.taraujo.compras.entidy;

public class Mercado {

	String nome;
	int idMercado;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdMercado() {
		return idMercado;
	}
	public void setIdMercado(int idMercado) {
		this.idMercado = idMercado;
	}
		
}
